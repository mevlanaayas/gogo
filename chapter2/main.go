
package main

import "fmt"

func main() {
	// integer addition
	fmt.Println("1 + 1 =", 1 + 1)

	//floating number addition
	fmt.Println("1 + 1 =", 1.0 + 1.0)

	// string ops
	fmt.Println(len("Hello, World"))
	fmt.Println("Hello, World"[1])
	fmt.Println("Hello, " + "World")

	// George Boole ops
	fmt.Println(true && true)
	fmt.Println(true && false)
	fmt.Println(true || true)
	fmt.Println(true || false)
	fmt.Println(!true)

	fmt.Println("Multiplication of 32,132 and 42,452 =", 32.132 * 42.452)

}

